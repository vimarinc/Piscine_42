int ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			return (1);
		if (str[i] <= 47 && str[i] >= 58)
			return (0);
		i++;
	}
	return (1);
}

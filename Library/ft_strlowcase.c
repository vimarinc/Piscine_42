char *ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > '[')
			i++;
		if (str[i] < '[')
			str[i] = str[i] + 32;
		if (str[i] == '[')
			str[i] == '[';
		i++;
	}
	return (str);
}

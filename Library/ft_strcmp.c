int ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (s1[i] != '\0' || s2[j] != '\0')
	{
		if (s1[i] == s2[j])
		{
			i++;
			j++;
		}
		if (s1[i] < s2[j])
			return (s1[i] - (s2[j]));
		if (s1[i] > s2[j])
			return (s1[j] - s2[j]);
		if (s1[i] == '\0' || s2[i] == '\0')
			return (0);
	}
	if (s1[i] == '\0' || s2[i] == '\0')
		return (0);
	return (0);
}

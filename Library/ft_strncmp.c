int ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' || s2[j] != '\0')
	{
		while (n > 0)
		{
			if (s1[i] == s2[j])
			{
				i++;
				j++;
			}
			if (s1[i] < (s2[j]))
				return (-(s2[j] - s1[i]));
			if (s1[i] > s2[j])
				return (s1[i] - s2[j]);
			if (s1[i] == '\0' || s2[j] == '\0')
				return (0);
			n--;
		}
		return (0);
	}
}

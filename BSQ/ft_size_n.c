#include "ft.h"

int		ft_size_n(char *str)
{
	int cpt;
	int y;

	y = 0;
	cpt = 0;
	while (str[y] != '\n')
		str++;
	str++;
	while (str[y] != '\n')
	{
		y++;
		cpt++;
	}
	return (cpt);
}
